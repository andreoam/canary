    getSumOfBonus = function(self, player)
        local bonus = {}
        local name = {}
        local mountName = {}
        for outfitId, data in pairs(self.Outfits) do
            if player:hasOutfit(outfitId, 3) then
                local outfitBonus = {}
                for key, value in pairs(data.bonus) do
                    if not bonus[key] then
                        bonus[key] = 0
                        outfitBonus[key] = 0
                    end
                    bonus[key] = bonus[key] + value
                    outfitBonus[key] = value

                    if self:getLimitByBonus(player, key) < bonus[key] then
                        bonus[key] = self:getLimitByBonus(player, key)
                        outfitBonus[key] = self:getLimitByBonus(player, key)
                    end
                end
                name[data.name] = outfitBonus
            end
        end

        for mountId, data in pairs(self.Mounts) do
            if player:hasMount(mountId) then
                local mountBonus = {}
                for key, value in pairs(data.bonus) do
                    if not bonus[key] then
                        bonus[key] = 0
                        mountBonus[key] = 0
                    end
                    bonus[key] = bonus[key] + value
                    mountBonus[key] = value

                    if self:getLimitByBonus(player, key) < bonus[key] then
